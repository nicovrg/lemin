#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*dest;
	int		i;

	i = 0;
	dest = NULL;
	if (s1)
	{
		if (!(dest = (char *)malloc(sizeof(char) * ft_strlen(s1) + 1)))
			return (NULL);
		while (s1[i] != '\0')
		{
			dest[i] = s1[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (dest);
}
