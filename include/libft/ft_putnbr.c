#include "libft.h"

void	ft_putnbr(int n)
{
	if (n >= 0 && n <= 9)
		ft_putchar(n + '0');
	if (n == -2147483648)
	{
		ft_putchar('-');
		ft_putchar('2');
		ft_putnbr(147483648);
	}
	if (n > -2147483648 && n < 0)
	{
		ft_putchar('-');
		ft_putnbr(-n);
	}
	if (n >= 10 && n <= 2147483647)
	{
		ft_putnbr(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
